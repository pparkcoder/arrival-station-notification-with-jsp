package login;

public class Station {
	private int station_num;
	private int station_line;
	private String station_name;
	private double station_x;
	private double station_y;
	
	public int getStation_num() {
		return station_num;
	}
	public void setStation_num(int station_num) {
		this.station_num = station_num;
	}
	public int getStation_line() {
		return station_line;
	}
	public void setStation_line(int station_line) {
		this.station_line = station_line;
	}
	public String getStation_name() {
		return station_name;
	}
	public void setStation_name(String station_name) {
		this.station_name = station_name;
	}
	public double getStation_x() {
		return station_x;
	}
	public void setStation_x(double station_x) {
		this.station_x = station_x;
	}
	public double getStation_y() {
		return station_y;
	}
	public void setStation_y(double station_y) {
		this.station_y = station_y;
	}
}